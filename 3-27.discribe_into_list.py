t = (1, 2, 3, 4, 5)

r = []
for i in t:
    r.append(i)

print(r)
"""
[1, 2, 3, 4, 5]
"""

r = [i for i in t]
print(r)
"""
[1, 2, 3, 4, 5]
"""


s = []
for i in t:
    if i % 2 == 0:
        s.append(i)
print(s)
"""
[2, 4]
"""

s = [i for i in t if i % 2 == 0]
print(s)
"""
[2, 4]
"""


